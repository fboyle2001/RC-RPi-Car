package com.finlay.rcsim.ui;

public class Colour {

	private float red;
	private float green;
	private float blue;
	
	public Colour(float red, float green, float blue) {
		this.red = red;
		this.green = green;
		this.blue = blue;
	}
	
	public float getRed() {
		return red;
	}
	
	public float getGreen() {
		return green;
	}
	
	public float getBlue() {
		return blue;
	}
	
}
